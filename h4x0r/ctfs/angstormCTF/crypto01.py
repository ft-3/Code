enc = 'Pxevhfx mh tgzlmkhfvmy. Px ahix rhn xgchr hnk vmy. tvmy{utvd_mh_max_ynmnkx}.'

def lol(x):
    d = {
            't': 'f',
            'm': 'a',
            'v': 'l',
            'y': 'g'
        }
    try:
        return d[x]
    except:
        return x

for char in enc:
    print(lol(char), end='')

